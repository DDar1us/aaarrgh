package aaarrgh.model;

public class Seguimiento {

	private Integer idSeguidor;
	private Integer idSeguido;
	private String estadoSeguidor;
	private String estadoSeguido;
	
	public Integer getIdSeguidor() {
		return idSeguidor;
	}
	public void setIdSeguidor(Integer idSeguidor) {
		this.idSeguidor = idSeguidor;
	}
	public Integer getIdSeguido() {
		return idSeguido;
	}
	public void setIdSeguido(Integer idSeguido) {
		this.idSeguido = idSeguido;
	}
	public String getEstadoSeguidor() {
		return estadoSeguidor;
	}
	public void setEstadoSeguidor(String estadoSeguidor) {
		this.estadoSeguidor = estadoSeguidor;
	}
	public String getEstadoSeguido() {
		return estadoSeguido;
	}
	public void setEstadoSeguido(String estadoSeguido) {
		this.estadoSeguido = estadoSeguido;
	}
	
	
}
